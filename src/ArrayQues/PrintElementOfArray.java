package ArrayQues;

public class PrintElementOfArray {
    public static void main(String[] args) {
        int[] array = {1, 2, 3, 4, 5};

        // Print each element of the array using a for loop
        for (int i = 0; i < array.length; i++) {

            System.out.println(array[i]);
        }
    }
}
